"""
Python = Liguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Guilherme painko scalcon')
print('Guilherme "painko" scalcon')

# Aspas duplas 
print("Guilherme painko scalcon")
print("Guilherme 'painko' scalcon")

# Escape
print("Guilherme \"painko\" scalcon")

# r
print(r"Guilherme \"painko\" scalcon")
